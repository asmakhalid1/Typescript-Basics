//for loop
for(let i=0;i<6;i++){
    console.log(i);
}
for(let j=10;j>=1;j--){
    console.log(j);
}

//while loop
let count = 1;
while(count<=10){
    console.log(count);
    count++;
}

//loop control statement
//break
for(let i=1;i<6;i++){
    if(i==3){
        break;
    }
    console.log(i);
}

//continue
for(let i=1;i<6;i++){
    if(i==3){
        continue;
    }
    console.log(i);
}

